    public function getCity($id)
    {
        $city_list = array();
        $cities = DB::table('states_cities')->where('state_id', $id)->get();

        foreach($cities as $city)
            $city_list[$city->city_id]=$city->city;

        return Response::json(array(
                'error' => false,
                'html' =>Form::select('city',$city_list , 0, array('class' => 'form-control'))),
            200
        );
    }


